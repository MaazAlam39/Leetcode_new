class Solution {
public:
    vector<int> minOperations(string boxes) {
        vector<int>ans(boxes.length());
        for(int curr=0;curr<boxes.length();curr++){
            if(boxes[curr]=='1'){
                for(int newp=0;newp<boxes.length();newp++)
                ans[newp]+=abs(newp-curr);
            }
        }
         return ans;    
        
    }
};



















































































































































































































































































































































































































































































































































































































































































































































































































































